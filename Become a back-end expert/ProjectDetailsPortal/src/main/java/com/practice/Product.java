package com.practice;

public class Product {
	private int pID;
	private String pName;
	private long pPrice;
	private String pFreshness;
	
	public int getpID() {
		return pID;
	}
	public void setpID(int pID) {
		this.pID = pID;
	}
	public String getpName() {
		return pName;
	}
	public void setpName(String pName) {
		this.pName = pName;
	}
	public long getpPrice() {
		return pPrice;
	}
	public void setpPrice(long pPrice) {
		this.pPrice = pPrice;
	}
	public String getpFreshness() {
		return pFreshness;
	}
	public void setpFreshness(String pFreshness) {
		this.pFreshness = pFreshness;
	}
	
}
